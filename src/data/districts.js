export const getAllDistricts = () => {
  const allDistricts = [];
  Object.values(districtsData).forEach(zone => {
    zone.districts.forEach(district => {
      allDistricts.push(district.name);
    });
  });
  return allDistricts.sort();
};

export const getPoliceStationsByDistrict = (districtName) => {
  for (const zone of Object.values(districtsData)) {
    const district = zone.districts.find(d => d.name === districtName);
    if (district) {
      return district.policeStations;
    }
  }
  return [];
}; 